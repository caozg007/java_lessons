package 第1部分;

/**
 * 布尔型、整数型、浮点型、字符型，还有常用的字符串类型
 * 编写程序，定义以上类型的各个变量，随意的赋值，然后输出。
 *
 * @author LiuDelin
 */
public class A_08_1 {

	public static void main(String [] args){
		
		boolean bool_type = true;
		int int_type = 10;
		float float_type = 2.4F;
		char char_type = 'c';
		String string_type = "str";

		System.out.println("bool_type:" + bool_type);
		System.out.println("int_type:" + int_type);
		System.out.println("float_type:" + float_type);
		System.out.println("char_type:" + char_type);
		System.out.println("string_type:" + string_type);
	}
}
